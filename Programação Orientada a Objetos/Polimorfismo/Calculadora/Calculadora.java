package Polimorfismo.Calculadora;

public class Calculadora {

    //somar
    public int somar (int i, int j){
        return i + j;
    }

    public double somar(double d, int i){
        return d + i;
    }

    public double somar(int i, int j, int k){
        return i + j + k;
    }

    //multiplicar
    public int multiplicar(int i, int j){
        return i * j;
    }

    public double multiplicar (double d, double f){
        return d * f;
    }
}
